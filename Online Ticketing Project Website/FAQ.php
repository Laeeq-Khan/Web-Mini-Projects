   <strong> Q: WHEN I CAN MAKE AN ADVANCE RESERVATION?</strong><br>
A: Advance reservation starts 96 hours before the departure till half an hour before.Reservation will be active till 30 minutes before the departure. <br> <br>

<strong>Q: FOR HOW MUCH TIME PERIOD THIS RESERVATION WILL STAY ACTIVE?</strong><br>
A: In case of reservation of less than 4 seats, the reservation remains active till fifteen minutes before the departure. All reservations are cancelled after this time (i.e. fifteen minutes before the departure). In case of more seats, a grace period is specified for the purchase of tickets, after which the reservation is cancelled.
<br> <br>
<strong>Q: HOW CAN I MAKE AN ADVANCE RESERVATION?</strong><br>
A: Advance reservation can be made by two ways 1. Through making a phone call at information and booking center of that particular terminal 2. Through reservation window at that terminal.
<br> <br>
<strong>Q: ON WHICH TELEPHONE NUMBER I SHOULD CALL TO RESERVE SEAT OR GET INFORMATION?</strong><br>
A: At all of our main terminals, U.A.N. Number i.e. 111-007-008 is being operated. You can call to this number from your local city or can use the city code in prefix where you want to call. However at sub-terminals, contact numbers are different for each city and thus get numbers through this website in Terminals Section.
<br> <br>
<strong>Q: AFTER RESERVATION, WHEN I CAN PURCHASE THE TICKET?</strong><br>
A: Ticket can be purchased at any time after reservation till fifteen minutes before the departure. All reservations are cancelled and seat can be issued to other passengers after this time (i.e. fifteen minutes before the departure).
<br> <br>
<strong>Q: FROM WHERE I CAN PURCHASE THE TICKET?</strong><br>
A: Ticket can be purchased from the advance ticketing counter till half an hour before the departure. Current ticketing starts at half an hour before the departure, tickets can be purchased from the specific ticketing counters.
<br> <br>
<strong>Q: CAN I SEND SOME GOODS TO OTHER CITIES USING THIS BUS SERVICE?</strong><br>
A: Daewoo has a separate cargo division. For delivery of goods, cargo service is available 24 x 7 at each terminal. Moreover Cargo Pick up Points are also available in Lahore, Rawalpindi, Multan and Faisalabad.
